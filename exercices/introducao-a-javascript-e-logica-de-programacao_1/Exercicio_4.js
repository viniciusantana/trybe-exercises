/*
Faça um programa que, dado um valor definido numa variável, retorne "positive" se esse valor for positivo, "negative" se for negativo e "zero" caso contrário. 
*/
let a = 1;

if (a > 0) {
    console.log('Positive');
} else if (a < 0) {
    console.log('Negative');
} else {
    console.log('Zero');
}